     &+ LOG10(P/P0)*(B(K) + B1(K)*LOG10(T) + B2(K)*LOG10(T)**2)
     &+             (C(K) + C1(K)*LOG10(T) + C2(K)*LOG10(T)**2)
     & *LOG10(P/P0)**2
       GASMIX=C2H2(J)
       IF (K .GT. 11) GASMIX=C2H6(J)
       XKV=10.**XKV
